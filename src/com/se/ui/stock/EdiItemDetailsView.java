    private void keyAction() {
        if (eQty == Integer.parseInt(spinnerQty.getValue().toString()) && eCategory.equals(txtCategory.getText()) && eItemName.equals(txtName.getText()) && eUPrice.equals(txtPrice.getText()) && eWarranty.equals(txtWarranty.getText())) {
            btnSave.setText("OK");
        } else {
            btnSave.setText("Save");
        }
    }
