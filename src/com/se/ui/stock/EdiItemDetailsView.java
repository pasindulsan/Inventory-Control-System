    void initializeView(int itemId) {
        list = ItemSQL.getAllItems();
        this.itemId = itemId;
        display(itemId);
        btnSave.setText("OK");
        eCategory = txtCategory.getText();
        eItemName = txtName.getText();
        eQty = Integer.parseInt(spinnerQty.getValue().toString());
        eUPrice = txtPrice.getText();
        eWarranty = txtWarranty.getText();
    }

    public void display(int id) {
        list.forEach((Items items) -> {
            if (items.getItem_id() == id) {
                String category = items.getCategory();
                String item_name = items.getItem_Name();
                int net_qty = items.getNet_qty();
                double unit_price = items.getUnit_price();
                String warranty = items.getWarranty();
                txtCategory.setText(category);
                txtName.setText(item_name);
                spinnerQty.setValue(net_qty);
                txtPrice.setText(String.valueOf(unit_price));
                txtWarranty.setText(warranty);
            }
        });
    }

    private void cmbWarrantyAction(int warranty) {

        switch (warranty) {
            case 7:
                txtWarranty.setEnabled(true);
                txtWarranty.setBorder(BorderFactory.createLineBorder(ColorPalette.BTNBORDER, 1));
                txtWarranty.setText("");
                break;
            case 0:
                txtWarranty.setEnabled(false);
                txtWarranty.setBorder(BorderFactory.createLineBorder(java.awt.Color.LIGHT_GRAY, 1));
                txtWarranty.setText("");
                break;
            default:
                txtWarranty.setEnabled(false);
                txtWarranty.setBorder(BorderFactory.createLineBorder(java.awt.Color.LIGHT_GRAY, 1));
                txtWarranty.setText((String) cmbWarranty.getSelectedItem());
                break;
        }

    }

